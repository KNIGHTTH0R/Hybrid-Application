3. 수집한 개인정보의 이용
</font>
<div class="content">
회사는 아래의 목적에 한해 개인정보를 이용합니다.<br>

1) 회원 식별, 가입의사 및 탈퇴의사 확인, 부정이용 및 중복가입 방지, 재학생 및 졸업생 확인 등 회원관리<br>
2) 시간표, 학생증, 벼룩시장 등 대학생 맞춤 서비스 개발·제공·개선<br>
3) 강의, 학과, 관심사 등 인구통계학적 자료 분석을 통한 맞춤형 서비스 및 광고 제공<br>
4) 회원 문의/제안/제휴/협력/게시 요청 처리, 공지사항 전달<br>
5) 구매, 판매, 배송, 공급, 지급, 제작, 중개, 중계, 당첨 등 회사와 회원 간 체결된 약정 이행<br>
</div>
