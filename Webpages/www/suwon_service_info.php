4. 개인정보의 제공 및 위탁
</font>
<div class="content">
회사는 원칙적으로 회원의 동의 없이 개인정보를 외부에 제공·위탁하지 않습니다. 단, 아래의 경우에 한해 제휴사, 국가기관 등에 제공될 수 있습니다.<br>

1) 휴대폰 번호로 본인확인을 할 경우<br>
- 수탁업체: 해당사항 없음<br>
- 개인정보의 보유 및 이용기간: 본인확인 업체에서 이미 보유하고 있는 개인정보이기 때문에 별도로 저장하지 않음<br>
2) 회원이 회사와 약정을 체결할 경우<br>
3) 회원의 생명이나 안전에 급박한 위험이 확인되어 이를 해소하기 위한 경우<br>
4) 기타 관련 법령에 의해 개인정보 제공 의무가 발생한 경우 등<br>
※ 개인정보가 아닌, 회원을 식별할 수 없게 단독으로 암호화된 Advertising ID (AAID, IDFA 등)는 기타 마케팅 채널에서 이용될 수 있습니다.
