5. 수집한 개인정보의 보관 및 파기
</font>
<div class="content">
개인정보 수집 및 이용목적이 달성된 후, 각 목적에 따라 수집된 개인정보는 일정 기간 이후 파기됩니다.<br>

1) 학교 인증: 즉시<br>
2) 제휴/협력/게시 요청: 최대 7일<br>
3) 이벤트 참여 및 당첨: 최대 30일<br>
4) 그 외: 최대 1년<br>
단, 법령에 의해 항목을 보관할 경우, 해당 법령에서 정한 최대 기간만큼 저장합니다.
파기된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제합니다.
