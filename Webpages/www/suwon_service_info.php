2. 수집하는 개인정보의 항목
</font>
<div class="content">
회원은 회원가입을 하지 않더라도, 수원톡톡이 제공하는 서비스를 회원과 동일하게 이용할 수 있습니다. 단, 개인정보를 필요로 하는 서비스는 이용 불가능합니다.
회원은 본인의 선택으로 회원가입을 한 뒤 서비스를 이용할 수 있습니다. 이 경우, 회사는 회원가입 시점부터 아래와 같은 개인정보 중 일부를 수집합니다. 단, 회원이 각 항목에 해당되지 않을 경우, 해당 항목의 개인정보는 절대 수집하지 않습니다.<br>

1) 회원이 회원가입 또는 본인확인을 할 경우<br>
- 학교, 아이디, 이메일, 휴대폰 번호, 성별, 나이, 이름, 닉네임, 입학년도, 프로필 사진, 마케팅 수신 동의 여부, 페이스북 고유번호<br>
2) 회원이 학교인증을 할 경우<br>
- 학번, 학교 이메일, 학생증, 학생증 바코드, 학교 증명서<br>
3) 회원이 회사와 약정을 체결할 경우<br>
- 이름, 주소, 연락처<br>
4) 회원이 본인의 주소록을 이용하여 친구 초대·추가 기능을 사용할 경우<br>
- 주소록 내에 저장된 이름 및 전화번호<br>
5) 회원이 제휴/협력 및 게시 요청을 할 경우<br>
- 단체명, 이름, 직책, 이메일, 연락처<br>

※ 각 항목 또는 추가적으로 수집이 필요한 항목의 개인정보는 도움말을 통한 회원 지원 과정 및 별도의 알림창을 통해서 서비스 이용 중 수집될 수 있습니다.<br>
※ 서비스 이용 과정에서 IP주소, 시간표, 할일, Advertising ID (AAID, IDFA 등) 등 서비스 이용기록과 기기정보가 자동으로 생성되어 수집될 수 있습니다.<br>
※ 회사는 인종, 사상, 의료정보 등 회원의 민감정보를 절대로 수집하지 않습니다.<br>
</div>
